#include"CSceneScreen.h"

#include"CTexture.h"


//スクリーンのポインタ
CSceneScreen* CSceneScreen::spInstance = nullptr;

CSceneScreen::CSceneScreen()
{
	Screen.Load("screen.tga");
	spInstance = this;
	w = 510;
	h = 800;
	mTag = ESCREEN;
}

void CSceneScreen::Update()
{
	y += speed;
		if (y == -1200)
			y = 1736;
}

void CSceneScreen::Render()
{
	if (mEnabled)
	{
		CRectangle::Render(Screen, 0, 255, 255, 0);
	}
		
	
	

}
bool CSceneScreen::Collision(CRectangle& r) {
	//mEnabledがfalseなら戻る
	if (!r.mEnabled)return false;
	if (!mEnabled) return false;
	//親のCollisionメソッドを呼び出す
	if (CRectangle::Collision(r)) {
		switch (r.mTag) {
		case EPLAYERBULLET:
			//プレイヤーの弾に当たると、無効にする
			mEnabled = false;
			r.mEnabled = false;
		case EPLAYER:
			//プレイヤーに当たると、無効にする
			mEnabled = false;
			break;
		}
		return true;
	}
	return false;
}
